package dto.t24.services;

public class FCDocIdentidad {
    
  
    private String  idclie;
    private String  doctype;
    private String  id;
    private String  doctypete;
    private String  doctypeib;
      
    private String errcod;
    private String detcod;
    private String defcod;

    /**
     * @return the idclie
     */
    public String getIdclie() {
        return idclie;
    }

    /**
     * @param idclie the idclie to set
     */
    public void setIdclie(String idclie) {
        this.idclie = idclie;
    }

    /**
     * @return the doctype
     */
    public String getDoctype() {
        return doctype;
    }

    /**
     * @param doctype the doctype to set
     */
    public void setDoctype(String doctype) {
        this.doctype = doctype;
    }

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the errcod
     */
    public String getErrcod() {
        return errcod;
    }

    /**
     * @param errcod the errcod to set
     */
    public void setErrcod(String errcod) {
        this.errcod = errcod;
    }

    /**
     * @return the detcod
     */
    public String getDetcod() {
        return detcod;
    }

    /**
     * @param detcod the detcod to set
     */
    public void setDetcod(String detcod) {
        this.detcod = detcod;
    }

    /**
     * @return the defcod
     */
    public String getDefcod() {
        return defcod;
    }

    /**
     * @param defcod the defcod to set
     */
    public void setDefcod(String defcod) {
        this.defcod = defcod;
    }

    /**
     * @return the doctypete
     */
    public String getDoctypete() {
        return doctypete;
    }

    /**
     * @param doctypete the doctypete to set
     */
    public void setDoctypete(String doctypete) {
        this.doctypete = doctypete;
    }

    /**
     * @return the doctypeib
     */
    public String getDoctypeib() {
        return doctypeib;
    }

    /**
     * @param doctypeib the doctypeib to set
     */
    public void setDoctypeib(String doctypeib) {
        this.doctypeib = doctypeib;
    }


   
}
